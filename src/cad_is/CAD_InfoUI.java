    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {                                       
      {                                              
        // TODO add your handling code here:
        try{
            String productId = txtId.getText().trim();
            String name = txtName.getText();
            
            String category = comboBox.getSelectedItem().toString();
            String recommendation = textRecommendation.getText();
            int price= Integer.parseInt(txtPrice.getText());
            if(price<=0){
                JOptionPane.showMessageDialog(rootPane,"Please enter valid price.","INVALID",JOptionPane.ERROR_MESSAGE);
                return;
            }
            String ranges=null;
            if(radMedium.isSelected()){
                ranges = "Medium";
            }
            else if(radLow.isSelected()){
                ranges ="Low";
            }
            else{
                ranges ="High";
            }
            String range=ranges;
            
            if(productId.isEmpty()||name.isEmpty()||category.isEmpty()||recommendation.isEmpty()||range.isEmpty()||txtPrice.getText()==""){
                JOptionPane.showMessageDialog(rootPane,"Please make sure all the inputs are filled.","INVALID",JOptionPane.ERROR_MESSAGE);
            }
            else{
                boolean flag = false;
                if(tblInfo.getRowCount()==0){
                    flag = true; 
                } else if(tblInfo.getRowCount()>0){
                    for(CarDetails s:list){
                        if(s.getProductId().equals(productId)){
                            flag = false;
                            JOptionPane.showMessageDialog(rootPane, "Error!!");
                            break;
                        } else {
                            flag = true;
                        }                    
                    }
                }
                if(flag){
                    DefaultTableModel model = (DefaultTableModel) tblInfo.getModel();
                    model.addRow(new Object[]{productId,name,range,category,recommendation, price});
                    CarDetails object = new CarDetails(productId,name,range,category,recommendation,price);
                    list.add(object);
                }

            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane,"Please verify if all the details are correct.","INVALID",JOptionPane.ERROR_MESSAGE);
        }
      }
    }   
    
    public static void selectionSort(ArrayList<CarDetails> sortData,int price){
        for(int i=0;i<sortData.size();i++){
            int minPos = minimumPosition(sortData,i);
            CarDetails temp = sortData.get(i);
            sortData.set(i,sortData.get(minPos));
            sortData.set(minPos,temp);
        }
    }
    
    public static int minimumPosition(ArrayList<CarDetails> sortData,int from){
        int minPos = from;
        for (int i = from + 1; i < sortData.size(); i++)
      {
          if (sortData.get(i).getPrice() < sortData.get(minPos).getPrice()){
                  minPos = i; 
              }
      }
      return minPos;
    }
    
