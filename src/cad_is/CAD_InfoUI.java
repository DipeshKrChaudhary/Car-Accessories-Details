    private void btnCategorySearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCategorySearchActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tblInfo.getModel();
        String categorySearch = (String) comboBoxSearch.getSelectedItem();
        if(model.getRowCount()==0){
            JOptionPane.showMessageDialog(rootPane,"Please add some accessories first.","Invalid",JOptionPane.ERROR_MESSAGE);
            return;
        }
        else{
            int count=0;

            for(CarDetails detail:list){
                if(detail.getCategory()==categorySearch){
                    count++;
                }
                else{
                    JOptionPane.showMessageDialog(rootPane,"No such accessory found.","ERROR",JOptionPane.ERROR_MESSAGE);
                }
            }
            JOptionPane.showMessageDialog(rootPane,"There are "+count+" items with this category","Result",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnCategorySearchActionPerformed

